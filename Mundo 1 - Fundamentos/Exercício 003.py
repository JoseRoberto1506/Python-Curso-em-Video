# Crie um programa que leia dois números e mostre a soma entre eles.

# O usuário digitará o primeiro número
number1 = int(input("Digite um número: "))

# O usuário digitará o segundo número
number2 = int(input("Digite outro número: "))

# Variável para somar os dois números digitados
sum = number1 + number2

print(f"A soma entre {number1} e {number2} é igual a {sum}.")